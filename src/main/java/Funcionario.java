public class Funcionario {

    public int id;
    public String estadoCivil;
    public String grau;
    public int filhos;
    public double salario;

    public Funcionario(int id, String estadoCivil, String grau, int filhos, double salario) {
        this.id = id;
        this.estadoCivil = estadoCivil;
        this.grau = grau;
        this.filhos = filhos;
        this.salario = salario;
    }
}
